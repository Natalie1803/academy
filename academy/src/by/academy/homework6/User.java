package by.academy.homework6;

public class User {
	private String name;
	private String surname;
	private int age;

	public User(String name, String surname, int age) {
		this.name = name;
		this.surname = surname;
		this.age = age;
	}

	
	public String getName() {
		return name;
	}


	public String getSurname() {
		return surname;
	}


	
}
